			<h1>La Commission Emploi</h1>
			<p>La Commission Emploi a pour objectif de rapprocher les offres des adhérents des demandes des entreprises.
			</p>
			<h2>Les missions</h2>
			<ul>
				<li>collecter et faire connaître aux adhérents les offres d’emploi qui parviennent à CPE par un contact direct  avec les entreprises suite aux opérations terrains, salons ou demande des entreprises avec qui un lien a été créée,
				</li>
				<li>créer et entretenir un lien avec des entreprises partenaires de CPE,</li>
			</ul>
			<h2>Les moyens</h2>
			<p>La Commission Emploi met à la disposition des adhérents les outils suivants :</p>
			<ul>
				<li>un fichier papier des offres d’emploi "cachées", trouvées directement par les adhérents lors des opérations terrains et la prospection des salons professionnels, en libre-service au secrétariat (classeur rouge pour l’année en cours),
				</li>
				<li>un fichier informatique dans la rubrique libre-service cataloguant les offres par année, mois, semaine, présentant les offres d'emploi provenant d'autres sources,
				</li>
				<li>des annuaires professionnels et catalogues d’expositions, de salons professionnels classés dans l’armoire du secrétariat et consultables sur place uniquement,
				</li>
				<li>permanence de la Commission Emploi le mardi matin de 10h à 12h : informations et conseils auprès des adhérents,
				</li>
				<li>entretiens individuels permettant à l'adhérent de  faire le point sur son projet, sur ses outils de communication
				</li>
			</ul>
			<h2>Les procédures</h2>
			<h3>offres d'emploi</h3>
			<p>La collecte des offres d’emploi provient de plusieurs sources :</p>
			<ul>
				<li>Les fiches récoltées à l’issue des opérations terrains et des salons,</li>
				<li>Les résultats de veilles effectuées par les adhérents sur plusieurs sites internet,</li>
				<li>Les offres parvenant directement par mail, courrier, téléphone à CPE.</li>
			</ul>
			<p>La Commission Emploi valide et enregistre les différentes offres et les met à disposition des adhérents en libre service au secrétariat, sous forme papier et informatique.
			</p>
			<p>Dés réception d’une offre intéressant un/des adhérents, la Commission Emploi met tout en œuvre pour prévenir le/les adhérents concernés dans les plus brefs délais. (email, téléphone)
			</p>
			<p>Les adhérents peuvent venir consulter les fichiers et la documentation pendant les heures normales d’ouverture du secrétariat ainsi qu'à l'occasion de la tenue de la permanence.
			</p>
			<h3>relations entreprise</h3>
			<p>La Commission Emploi assure le suivi des entreprises ; relance périodique pour détecter de nouveaux emplois. Elle entretient des relations suivies avec quelques entreprises à fort potentiel d’embauche, en particulier des cabinets spécialisés.
			</p>
			<h3>entretiens adhérents</h3>
			<p>La permanence de la Commission Emploi permet aux adhérents de recueillir conseils sur les axes de leur prospection, d'obtenir la communications d’adresses, de fichiers sources, etc.
			</p>
